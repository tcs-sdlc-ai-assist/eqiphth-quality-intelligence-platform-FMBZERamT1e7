/**
 * Returns aggregate statistics across all applications.
 *
 * @returns {{ totalApplications: number, totalTestCases: number, totalReleases: number, averageAutomationCoverage: number, averageQualityScore: number }} Aggregate application statistics
 */
export function getApplicationAggregates() {
  const totalApplications = applications.length;
  const totalTestCases = applications.reduce((sum, a) => sum + a.testCaseCount, 0);
  const totalReleases = applications.reduce((sum, a) => sum + a.releaseCount, 0);
  const averageAutomationCoverage =
    totalApplications > 0
      ? Math.round((applications.reduce((sum, a) => sum + a.automationCoverage, 0) / totalApplications) * 10) / 10
      : 0;

  const allReleases = applications.flatMap((a) => a.releases);
  const averageQualityScore =
    allReleases.length > 0
      ? Math.round((allReleases.reduce((sum, r) => sum + r.qualityScore, 0) / allReleases.length) * 10) / 10
      : 0;

  return {
    totalApplications,
    totalTestCases,
    totalReleases,
    averageAutomationCoverage,
    averageQualityScore,
  };
}

/**
 * Returns all unique technology stack items across all applications.
 *
 * @returns {string[]} Array of unique tech stack items sorted alphabetically
 */
export function getAllTechStacks() {
  const techStacks = new Set(applications.flatMap((a) => a.techStack));
  return [...techStacks].sort();
}

/**
 * Returns all unique application names.
 *
 * @returns {string[]} Array of application names sorted alphabetically
 */
export function getApplicationNames() {
  return applications.map((a) => a.name).sort();
}

